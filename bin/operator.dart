void main() {
  int num1 = 10;
  int num = 2;
  // +
  var result1 = num1 + num;
  print(result1);
  // -
  var result2 = num1 - num;
  print(result2);
  // *
  var result3 = num1 * num;
  print(result3);
  // /
  var result4 = num1 / num;
  print(result4);
  // %
  var result5 = num % num1;
  print(result5);
}
