void main() {
  int num1 = 10;
  int num2 = 9;

  // equal (==)
  
  if (num1 == num2) {
    print('true');
  } else {
    print('false');
  }

  //unequal (!=)

  if (num1 != num2) {
    print('true');
  } else {
    print('false');
  }

  //more than (>)
  if (num1 > num2) {
    print('true');
  } else {
    print('false');
  }

  //less than (<)
  if (num1 < num2) {
    print('true');
  } else {
    print('false');
  }

  //more than equally (>=)
  if (num1 >= num2) {
    print('true');
  } else {
    print('false');
  }

  // less than equally
  if (num1 <= num2) {
    print('true');
  } else {
    print('false');
  }

}
